    private void jLacuna1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLacuna1MouseClicked
        jogo.setJogada(0);
        jogo.Jogar(jogo.QuemJoga(),Dificuldade);
        this.setPosition();
        jogo.QuemVenceu();
        this.jLabel2.setText("Jogadas: "+jogo.getNumero_de_jogadas());
        this.jLabel3.setText("Vencedor: "+jogo.getVencedor());
        if(jogo.getVencedor()!=0){
            String[] opcoes = new String[3];
            opcoes[0]= new String("Reiniciar Esta Partida");
            opcoes[1]= new String("Iniciar uma nova partida");
            opcoes[2]= new String("Fechar jogo");
            int i=-1;
            while(i==-1){
                if(jogo.getVencedor()==1)
                    i= JOptionPane.showOptionDialog(rootPane, "Vencedor: Humano! Parabéns ", "Vencedor",0, JOptionPane.INFORMATION_MESSAGE, null,opcoes,null);
                if(jogo.getVencedor()==2)
                    i= JOptionPane.showOptionDialog(rootPane, "Vencedor: Máquina! %&%&%*! -A Máquina ri de você  ", "Vencedor",0, JOptionPane.INFORMATION_MESSAGE, null,opcoes,null);
                if(jogo.getVencedor()==3)
                    i= JOptionPane.showOptionDialog(rootPane, "Empatou!", "Vencedor",0, JOptionPane.INFORMATION_MESSAGE, null,opcoes,null);
            }
            if(i==0){
                jogo.Reiniciar_Partida();
            if(QuemComeca == 0)
            jogo.Escolher1();
                else{
                jogo.Escolher2();
                jogo.Jogar(jogo.QuemJoga(),Dificuldade);
                }
            this.setPosition();
            this.jLabel2.setText("Jogadas: "+0);
            this.jLabel3.setText("Vencedor: "+0);
            }
            else if(i==2){
                this.dispose();
            }
            else if(i==1){
                this.dispose();
                EscolherModo tela = new EscolherModo();
                tela.setVisible(true);
                 
            }
            
        }
        
    }//GEN-LAST:event_jLacuna1MouseClicked

