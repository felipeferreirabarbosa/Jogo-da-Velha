    public void DadosDaTela(int Escolha_, int Dificuldade_, int QuemComeca_){
        Escolha=Escolha_;
        Dificuldade = Dificuldade_;
        QuemComeca= QuemComeca_;
        if(QuemComeca == 0)
            jogo.Escolher1();
        else{
            jogo.Escolher2();
            jogo.Jogar(jogo.QuemJoga(),Dificuldade);
            jogo.QuemVenceu();
            this.jLabel2.setText("Jogadas: "+jogo.getNumero_de_jogadas());
            this.jLabel3.setText("Vencedor: "+jogo.getVencedor());     
        }
        this.setPosition();
    }
